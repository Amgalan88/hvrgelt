create table if not exists partners (
  id         text primary key,
  name       text not null,
  category   text not null,
  emoji      text not null default '📦',
  address    text not null,
  detail     text not null default '',
  area       text not null default '',
  active     boolean not null default true,
  created_at text not null
);

-- Realtime — газар нэмэхэд захиалагч дээр шууд шинэчлэгдэнэ
alter publication supabase_realtime add table partners;

-- RLS (демо — anon бүрэн эрхтэй; production-д хязгаарлана)
alter table partners enable row level security;
create policy "demo_all_partners" on partners for all using (true) with check (true);

